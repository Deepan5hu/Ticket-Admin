class CancelBookingIntentHandler(AbstractRequestHandler):
    
    def can_handle(self, handler_input):
        return ask_utils.is_intent_name("CancelBookingIntent")(handler_input)
        
    def handle(self, handler_input):
        BookingNumber = handler_input.request_envelope.request.intent.slots['BookingNumber'].value
        logger.info("BookingNumber"+BookingNumber)
            
        if int(BookingNumber) in range (1000000000,9999999999):
            speak_output = "Your Booking for "+BookingNumber+" has been Cancelled"
        
        else:
            speak_output = "Invalid Booking Number"
        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )


